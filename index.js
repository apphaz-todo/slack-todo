// ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
// Slash Command: /todo
// ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
app.command('/todo', async ({ command, ack, say }) => {
  console.log('üì• /todo command received')
  console.log('Command payload:', {
    text: command.text,
    user: command.user_id,
    channel: command.channel_id
  })

  try {
    await ack()
    console.log('‚úÖ ACK sent to Slack')

    const text = command.text.trim()

    if (text.startsWith('add')) {
      const title = text.replace(/^add/i, '').trim()

      console.log('üìù Adding task:', title)

      const { error } = await supabase.from('tasks').insert({
        title,
        created_by: command.user_id,
        assigned_to: command.user_id,
        channel_id: command.channel_id
      })

      if (error) {
        console.error('‚ùå Supabase insert error:', error)
        await say('‚ùå Failed to add task')
        return
      }

      await say(`‚úÖ Task added: *${title}*`)
      console.log('‚úÖ Task added successfully')
      return
    }

    if (text === 'list') {
      console.log('üìã Listing tasks')

      const { data, error } = await supabase
        .from('tasks')
        .select('*')
        .eq('assigned_to', command.user_id)
        .eq('status', 'open')

      if (error) {
        console.error('‚ùå Supabase fetch error:', error)
        await say('‚ùå Failed to fetch tasks')
        return
      }

      if (!data || data.length === 0) {
        await say('üéâ No open tasks')
        return
      }

      const list = data.map((t, i) => `${i + 1}. ${t.title}`).join('\n')
      await say(`üìù *Your Tasks*\n${list}`)
      console.log('‚úÖ Task list sent')
      return
    }

    await say('‚ùì Unknown command. Try `/todo add <task>` or `/todo list`')
  } catch (err) {
    console.error('üî• ERROR inside /todo handler:', err)
  }
})

